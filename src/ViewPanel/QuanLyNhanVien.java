    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        try {
            String Manv = txtManv.getText();
            String Tennv = txtTennv.getText();
            String User = txtUser.getText();
            String Pass = txtPass.getText();

            DBnhanvien pt = new DBnhanvien();
            if (isUpdateSelected && !Manv.isEmpty() && !Tennv.isEmpty() && !User.isEmpty() && !Pass.isEmpty()) {
                pt.Update(Manv, Tennv, User, Pass);
                JOptionPane.showMessageDialog(null, "Sửa thành công.");
            } else {
                if (pt.KiemTra(txtManv.getText())) {
                    JOptionPane.showMessageDialog(null, "Trùng mã sản phẩm. Vui lòng nhập lại.");
                    //return;
                } else {
                    if (isAddSelected && !Manv.isEmpty() && !Tennv.isEmpty() && !User.isEmpty() && !Pass.isEmpty()) {
                        pt.Insert(Manv, Tennv, User, Pass);
                        JOptionPane.showMessageDialog(null, "Thêm thành công.");
                    }
                }
            }

            loadTable();
            setReadOnly();

            isAddSelected = false;
            isUpdateSelected = false;

            txtManv.setText("");
            txtTennv.setText("");
            txtUser.setText("");
            txtPass.setText("");

        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "ERROR ClassNotFoundException: " + ex.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "ERROR SQLException: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnLuuActionPerformed


