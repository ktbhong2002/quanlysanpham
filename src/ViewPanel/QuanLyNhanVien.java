    public int Dem() {
        int dem = 0;
        try {
            DBnhanvien pt = new DBnhanvien();
            ResultSet rs = pt.GetData();
            while (rs.next()) {
                dem++;
            }

            pt.Close();
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "ERROR ClassNotFoundException: " + ex.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "ERROR SQLException: " + ex.getMessage());
        }
        return dem;
    }

    private void loadTable() {
        this.TblNhanVien.removeAll();
        DefaultTableModel model = (DefaultTableModel) TblNhanVien.getModel();
        model.setRowCount(Dem());
        try {
            DBnhanvien pt = new DBnhanvien();
            ResultSet rs = pt.GetData();
            int i = 0;
            while (rs.next()) {
                this.TblNhanVien.setValueAt(rs.getString("MaNV"), i, 0);
                this.TblNhanVien.setValueAt(rs.getString("TenNV"), i, 1);
                this.TblNhanVien.setValueAt(rs.getString("Username"), i, 2);
                this.TblNhanVien.setValueAt(rs.getString("Password"), i, 3);
                i++;
            }
            pt.Close();
        } catch (Exception ex) {
            //JOptionPane.showMessageDialog(null, "ERROR SQLException: " + ex.getMessage());
            System.out.println("Loi: " + ex.toString());
        }
    }

